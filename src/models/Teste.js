import { DataTypes } from 'sequelize';
import sequelize from '../config';

const Teste = sequelize.define(
  'teste',
  {
    id: {
      type: DataTypes.INTEGER,
      primaryKey: true,
      autoIncrement: true,
    },
  },
  {
    freezeTableName: true,
    timestamps: true,
    createdAt: 'created_at',
    updatedAt: 'updated_at',
  },
);

export default Teste;
